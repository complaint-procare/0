create table if not exists public.boxes (
  id uuid primary key default gen_random_uuid(),
  source_row integer not null unique,
  article text not null default '',
  name text not null default '',
  length_cm numeric(10,2) not null,
  width_cm numeric(10,2) not null,
  height_cm numeric(10,2) not null,
  volume_cm3 numeric(14,2) not null,
  original text not null default '',
  note text not null default '',
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint boxes_positive_dimensions check (length_cm > 0 and width_cm > 0 and height_cm > 0)
);

create index if not exists boxes_article_idx on public.boxes (article) where article <> '';
create index if not exists boxes_volume_idx on public.boxes (volume_cm3);
create index if not exists boxes_active_idx on public.boxes (is_active) where is_active;

alter table public.boxes enable row level security;

do $$
begin
  if not exists (
    select 1 from pg_policies
    where schemaname = 'public'
      and tablename = 'boxes'
      and policyname = 'boxes_anon_select'
  ) then
    create policy boxes_anon_select on public.boxes
      for select to anon using (true);
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public'
      and tablename = 'boxes'
      and policyname = 'boxes_authenticated_select'
  ) then
    create policy boxes_authenticated_select on public.boxes
      for select to authenticated using (true);
  end if;
end $$;

